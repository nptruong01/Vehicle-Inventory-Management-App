    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            JFileChooser jFileChooser = new JFileChooser();
            jFileChooser.showSaveDialog(this);
            File saveFile = jFileChooser.getSelectedFile();

            if(saveFile != null){
                saveFile = new File(saveFile.toString()+".xlsx");
                Workbook wb = new XSSFWorkbook();
                Sheet sheet = wb.createSheet("Quản Lý Kho Xe");

                Row rowCol = sheet.createRow(0);
                for(int i=0;i<xuatkhotable.getColumnCount();i++){
                    Cell cell = rowCol.createCell(i);
                    cell.setCellValue(xuatkhotable.getColumnName(i));
                }

                for(int j=0;j<xuatkhotable.getRowCount();j++){
                    Row row = sheet.createRow(j+1);
                    for(int k=0;k<xuatkhotable.getColumnCount();k++){
                        Cell cell = row.createCell(k);
                        if(xuatkhotable.getValueAt(j, k)!=null){
                            cell.setCellValue(xuatkhotable.getValueAt(j, k).toString());
                        }
                    }
                }
                FileOutputStream out = new FileOutputStream(new File(saveFile.toString()));
                wb.write(out);
                wb.close();
                out.close();
                openFile(saveFile.toString());
            }else{
                JOptionPane.showMessageDialog(null,"Thao Tác In Đã Hủy");
            }
        }catch(FileNotFoundException e){
            System.out.println(e);
        }catch(IOException io){
            System.out.println(io);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

